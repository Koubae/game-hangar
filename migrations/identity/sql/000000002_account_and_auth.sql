-- ///////////////////
-- // CREDENTIALS
-- ///////////////////
CREATE TABLE IF NOT EXISTS account_credentials
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    credential  TEXT        NOT NULL,
    account_id  UUID        NOT NULL REFERENCES account (id) ON DELETE CASCADE,
    provider_id BIGINT      NOT NULL REFERENCES provider (id) ON DELETE RESTRICT,

    secret      TEXT        NOT NULL,
    secret_type TEXT        NOT NULL DEFAULT 'password', -- sha256, pem, opaque

    verified    BOOLEAN     NOT NULL DEFAULT FALSE,
    verified_at TIMESTAMPTZ,

    disabled    BOOLEAN     NOT NULL DEFAULT FALSE,
    disabled_at TIMESTAMPTZ,

    created     TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated     TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    UNIQUE (provider_id, credential),
    UNIQUE (account_id, provider_id),
    CONSTRAINT check__verified_requires_timestamp
        CHECK ((verified = FALSE AND verified_at IS NULL) OR (verified = TRUE AND verified_at IS NOT NULL)),

    CONSTRAINT check__disabled_requires_timestamp
        CHECK ((disabled = FALSE AND disabled_at IS NULL) OR (disabled = TRUE AND disabled_at IS NOT NULL))
);
