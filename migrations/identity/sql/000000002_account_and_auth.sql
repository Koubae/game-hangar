-- // PROVIDERS
-- ///////////////////
CREATE TABLE IF NOT EXISTS provider
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source       CITEXT      NOT NULL,
    type         CITEXT      NOT NULL,
    display_name TEXT        NOT NULL,
    category     VARCHAR(64) NOT NULL,
    disabled     BOOLEAN     NOT NULL DEFAULT FALSE,

    created      TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated      TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,

    UNIQUE(source, type),
    CONSTRAINT check__source_length CHECK (CHAR_LENGTH(source) BETWEEN 3 AND 120),
    CONSTRAINT check__type_length CHECK (CHAR_LENGTH(type) BETWEEN 3 AND 120),
    CONSTRAINT check__display_name_length CHECK (CHAR_LENGTH(display_name) BETWEEN 3 AND 255),
    CONSTRAINT check__category_length CHECK (CHAR_LENGTH(category) BETWEEN 3 AND 64)
);
CREATE INDEX IF NOT EXISTS provider__index_category ON provider (category);
